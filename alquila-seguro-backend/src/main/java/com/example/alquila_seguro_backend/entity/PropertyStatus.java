/**
 * Constantes con los estados de una propiedad.
 */
public enum PropertyStatus {
    /**
     * Propiedad disponible para la reserva.
     */
    AVAILABLE,

    /**
     * Propiedad no disponible para la reserva.
     */
    UNAVAILABLE,

    /**
     * Propiedad reservada.
     */
    RESERVED

}
